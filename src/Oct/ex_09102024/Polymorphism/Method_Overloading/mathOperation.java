package Oct.ex_09102024.Polymorphism.Method_Overloading;

public class mathOperation {

    //Method Overloading

    int sum(int a, int b)
    {
        return a+b;
    }

    String sum(String a, String b)
    {
        return a+b;
    }

    int sum(int a,int b, int c)
    {
        return a+b+c;
    }
}
